/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  ubx_status ubx_status = UBX_OK;
  adxl345_status acc_status = ADXL345_OK;
  uint32_t cur_time = 0, start_time = 0;
  uint8_t out_buf[sizeof(out_header) + sizeof(error_output)] = {0};
  out_header *out_h = (out_header *)out_buf;
  error_output *output = (error_output *)(out_buf + sizeof(out_header));
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_SPI1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
  HAL_Delay(5000);
  if ((ubx_status = UBX_Init(DMA2, USART1))) {
    /* block main, because GPS is a core device */
    while (1) {}
  }
  if((acc_status = ADXL345_Init(DMA2, LL_DMA_STREAM_0, SPI1, Acc_CS_GPIO_Port, Acc_CS_Pin))) {
    /* notify but don't block main loop */
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    if (led_blink_flag) {
      led_blink_flag = 0;
      LL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
    }
    if (acc_int_flag) {
      adxl345_ProcessInterrupt();
    }
    if (gps_data_ready_flag) {
      gps_data_ready_flag = 0;
      UBX_ProcessData();
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

